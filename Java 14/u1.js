
	// JavaScript program to illustrate switch-case
	let i = 9;

	switch(i){
	case 0:
		document.writeln("i is zero.");
		break;
	case 1:
		document.writeln("i is one.");
		break;
	case 2:
		document.writeln("i is two.");
		break;
	default:
		document.writeln("i is greater than 2.");
	}

