	switch(i){
	case 0:
		document.writeln("i is zero.");
		break;
	case 10:
		document.writeln("i is one.");
		break;
	case 25:
		document.writeln("i is two.");
		break;
	default:
		document.writeln("i is bigger than 5.");
	}
